#include "calculator.h"

Calculator::Calculator() {}

int Calculator::add(int a, int b)
{
    return a + b;
}

double Calculator::multiplication(int a, int b)
{
    return a * b;
}

double Calculator::subtraction(int a, int b)
{
    return a - b;
}

double Calculator::division(int a, int b)
{
    return a / b;
}
